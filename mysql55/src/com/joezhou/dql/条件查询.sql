SELECT *
FROM EMP
WHERE ENAME = 'SMITH';

SELECT ENAME
FROM EMP
WHERE ENAME != 'SMITH';

SELECT ENAME
FROM EMP
WHERE ENAME <> 'SMITH';

SELECT ENAME, SAL
FROM EMP
WHERE SAL + 500 > 2000;

SELECT ENAME, SAL
FROM EMP
WHERE SAL > 2000
  AND SAL <= 3000;

SELECT ENAME, SAL, JOB
FROM EMP
WHERE SAL = 1250
  OR JOB = 'SALESMAN';
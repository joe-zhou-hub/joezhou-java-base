SELECT ENAME, SAL
FROM EMP
WHERE SAL IN (800, 1000, 1200, 1600);

SELECT ENAME, SAL
FROM EMP
WHERE SAL = 800
   OR SAL = 1000
   OR SAL = 1200
   OR SAL = 1600;

SELECT ENAME, SAL
FROM EMP
WHERE SAL NOT IN (800, 1000);

SELECT ENAME, SAL
FROM EMP
WHERE SAL != 800
  AND SAL != 1000;

SELECT ENAME, SAL
FROM EMP
WHERE SAL BETWEEN 2450 AND 3000;

SELECT ENAME, SAL
FROM EMP
WHERE SAL >= 2450
  AND SAL <= 3000;

SELECT ENAME, SAL
FROM EMP
WHERE SAL NOT BETWEEN 2450 AND 3000;

SELECT ENAME, SAL
FROM EMP
WHERE SAL < 2450
   OR SAL > 3000;